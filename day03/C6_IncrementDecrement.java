package day03;

public class C6_IncrementDecrement {

	public static void main(String[] args) {
		
		int number = 10;
		
		// bir variablenin deðerini toplayrak artýrmak için 3 yöntem kullanabilirim
		
		// 1.yontem (acemi yöntemi)

		number = number + 5;
		
		// 2. yontem(usta iþi) 
		
		number += 8;
		
		// 3.yontem sadece bir artýrmak için number ++;
		 
		number++;
		
		// bu yöntemler sadece toplama iþlemine has deðildir tüm operatörler için kullanýlabilir
		// 3 .yöntem sadece toplama ve çýkarma içindir
		
		// numberi 6 eksiltim 
		 
		number -= 6;
		// numberi 2 ye bölün
		
		number /= 2;
		 // numberý 3 ie çarpýn
		number *= 3;
		
		// numberý 1 eksiltin
		
		number--; // araya boþluk koyarak da yazabilirsin
		
		
		
		
	}

}
